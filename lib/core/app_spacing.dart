/// 8-point layout grid for consistent spacing across Tiny Pop screens.
abstract final class AppSpacing {
  static const unit = 8.0;

  static const xs = unit; // 8
  static const sm = unit * 2; // 16
  static const md = unit * 3; // 24
  static const lg = unit * 4; // 32
  static const xl = unit * 5; // 40
  static const xxl = unit * 6; // 48
}
